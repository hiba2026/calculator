package com.pantxi.calculator;

public class OperationsSimples {

    // Méthode d’addition
    public int addition(int a, int b) {
        return a + b;
    }

    // Méthode de soustraction
    public int soustraction(int a, int b) {
        return a - b;
    }

    // Méthode pour vérifier si un nombre est pair
    public boolean estPair(int n) {
        return n % 2 == 0;
    }

    // Méthode qui calcule la moyenne de deux nombres
    public double moyenne(double a, double b) {
        return (a + b) / 2.0;
    }
}
